namespace AspNetCoreNuxt.Extensions.AspNetCore.Mvc
{
    /// <summary>
    /// 集計演算子を表します。
    /// </summary>
    public enum AggregateOperator
    {
        /// <summary>
        /// 平均値を表します。
        /// </summary>
        Avg,
        /// <summary>
        /// 最大値を表します。
        /// </summary>
        Max,
        /// <summary>
        /// 最小値を表します。
        /// </summary>
        Min,
        /// <summary>
        /// 合計値を表します。
        /// </summary>
        Sum,
    }
}
